package com.comelec.items;

public class ItemAttribut {

	private  long id ;
	private  String nameItemAttribute ;
	private  long  Valeur;
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getNameItemAttribute() {
		return nameItemAttribute;
	}
	public void setNameItemAttribute(String nameItemAttribute) {
		this.nameItemAttribute = nameItemAttribute;
	}
	public long getValeur() {
		return Valeur;
	}
	public void setValeur(long valeur) {
		Valeur = valeur;
	} 
}
